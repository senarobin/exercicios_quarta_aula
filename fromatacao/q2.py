num_um = int(input('Digite um número: '))
num_dois = int(input('Digite outro número: '))

soma = num_um + num_dois
subtracao = num_um - num_dois
multiplicacao = num_um * num_dois
divisao = num_um / num_dois

print(f"A soma de {num_um} com {num_dois} é igual a {soma}")
print(f"A subtração de {num_um} com {num_dois} é igual a {subtracao}")
print(f"A multiplicação de {num_um} com {num_dois} é igual a {multiplicacao}")
print(f"A divisão de {num_um} com {num_dois} é igual a {divisao}")